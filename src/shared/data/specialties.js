export const featuredSpecialties = specialtyCatalog.filter((specialty) => specialty.featured);

const normalize = (value = "") =>
  value
    .toLowerCase()
    .replace(/&/g, "and")
    .replace(/[^a-z0-9]+/g, " ")
    .trim();

const buildTerms = (specialty) =>
  [specialty.slug, specialty.name, specialty.doctorFilter, ...(specialty.matchTerms || [])]
    .filter(Boolean)
    .map(normalize);

export const getSpecialtyBySlug = (slug) =>
  specialtyCatalog.find((specialty) => specialty.slug === slug);

export const findSpecialtyByQuery = (query = "") => {
  const normalizedQuery = normalize(query);
  if (!normalizedQuery) {
    return null;
  }

  return (
    specialtyCatalog.find((specialty) =>
      buildTerms(specialty).some(
        (term) =>
          term === normalizedQuery ||
          term.includes(normalizedQuery) ||
          normalizedQuery.includes(term)
      )
    ) || null
  );
};

export const matchesSpecialtyValue = (doctorSpecialty = "", value = "") => {
  if (!value) {
    return true;
  }

  const normalizedDoctorSpecialty = normalize(doctorSpecialty);
  const matchedSpecialty = findSpecialtyByQuery(value);
  const searchTerms = matchedSpecialty
    ? buildTerms(matchedSpecialty)
    : [normalize(value)];

  return searchTerms.some(
    (term) =>
      normalizedDoctorSpecialty.includes(term) ||
      term.includes(normalizedDoctorSpecialty)
  );
};
